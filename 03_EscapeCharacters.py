#  TO INSERT CHARACTERS THAT ARE ILLEGAL IN A STRING LITERAL, WE USE ESCAPE CHARACTERS
#IT IS FOLLOERD BY A BACKSLASH '\'AND A CHARACTER WHICH IS TO BE INSERTED
# SOME COMMON ESCAPE CHARACTERS ARE:
# \'  SINGLE QUOTE     
# \"  DOUBLE QUOTE
# \\  BACKSLASH 
# \b  BACKSPACE 
# \f  FORM FEED 
# \n  NEW LINE (ADDS A NEW LINE)
# \t  TAB  (ADDS A TAB SPACE)

#SINGLE QUOTE \'
print('I\'m  Suchita.' )

#DOUBLE QUOTE \"
print("He said \"Python is awesome\" ")
print('He saird"Python is awesome" ')

#BACKSLASH \\
print("This will insert one backslash \\ in the string.")
print("Path : C:\\Users\\Suchita\\Documents")

#NEW LINE \n
print("Hello World!\n Welcome to Python Programming.")
print("Hello World!\n Welcome to Python Programming.")

#TAB \t
print("Name\tAge\tCountry")

print("Alice\t30\tUSA")

#CAARRIAGE RETURN \r
print("Hello World! Welcome to Python Programming.\rHi")

#BACKSPACE \B
print("Hello \bWorld!")


